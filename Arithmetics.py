# Arithmetic operators

# * -> multiplication
# / -> division
# + -> addition
# - -> subtraction
# = -> assignment operator
# % -> remainder
# ** -> exponentiation
# // -> floor division (always rounded down to nearest integer)
# == -> comparison operator

print(17 % 3)
print(17/3)
print(17//3)

# check if something even or odd by using remainder in division by 2
print(100 % 2)
print(101 % 2)
print(102 % 2)
print(103 % 2)

