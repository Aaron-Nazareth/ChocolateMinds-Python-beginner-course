number_1 = 10
number_2 = 20

# Sum of two numbers
sum = number_1 + number_2
print("The sum of numbers 1 and 2 is: ", sum)

# Product of two numbers
product = number_1 * number_2
print("The product of numbers 1 and 2 is: ", product)

# Subtraction
subtraction = number_2 - number_1
print("The subtraction of number 1 from 2 is: ", subtraction)

# Reaminder of the division
# % <- "modulo" operator used for remainders
remainder = number_2 % number_1
print("The remainder from the division of number 2 by 1 is: ", remainder)

