number_one = 10
number_two = 20

# 1. Find out product of two numbers and print out

product = number_one * number_two
print(product)

# 2. Find out division of two numbers and print out

division = number_two / number_one
print(division)

# 3. Find out subtraction of two numbers and print out

subtraction = number_two - number_one
print(subtraction)

# 4. Can you do the division remainder too?

remainder = number_two % number_one
print(remainder)

